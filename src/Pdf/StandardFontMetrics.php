    public function measureString(string $fontAlias, float $fontSize, string $text): float
    {
        if ($text === '' || $fontSize <= 0.0) {
            return 0.0;
        }

        $width = 0.0;
        foreach ($this->splitCharacters($text) as $character) {
            $width += $this->resolveCharacterWidth($fontAlias, $character);
        }

        return ($width / 1000.0) * $fontSize;
    }

    private function resolveCharacterWidth(string $fontAlias, string $character): float
    {
        if (in_array($fontAlias, ['F5', 'F6'], true)) {
            return self::DEFAULT_WIDTH;
        }

        $widths = in_array($fontAlias, ['F3', 'F4'], true)
            ? self::TIMES_WIDTHS
            : self::HELVETICA_WIDTHS;

        return $widths[$character] ?? self::DEFAULT_WIDTH;
    }

    /**
     * @return list<string>
     */
    private function splitCharacters(string $text): array
    {
        $characters = preg_split('//u', $text, -1, PREG_SPLIT_NO_EMPTY);

        return $characters === false ? [] : $characters;
    }
